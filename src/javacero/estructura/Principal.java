package javacero.estructura;

/**
 *
 * Las estructura básica de un archivo .java es la siguiente: Java se estructura
 * mediante la programación orientada a objetos, eso quiere decir que todo está
 * compuesto o conformado en clases.
 *
 * Las clases en java se declaran poniendo: 
 * public class NombreClase {
 *      Código...
 * }
 *
 * Todo lo que desarrollemos irá dentro de las llaves de apertura y cierre de la
 * clase.
 * 
 * Los comentarios, que son porciones de texto que nunca se ejecutan, simplemente
 * se usan para dar información o explicar algo se describen así:
 * 
 * - Comentarios de una linea: // El comentario
 * - Comentarios de varias líneas: / * 
                                     * El comentario 
                                     * /
 * (Sin espacios)
 *
 * @author David Salazar
 */
public class Principal {

    // Este es el método principal, eso quiere decir, que todo el proyecto
    // iniciará aquí. La máquina virtual de java buscará siempre este método
    // para ejecutarlo en primer lugar, y a partir de aquí, ya se hace lo demás
    // que hayamos programado. Es obligatorio escribirlo tal cual.
    
    
    public static void main(String[] args) {
        // Desde aquí ya podemos programar
    }

}
